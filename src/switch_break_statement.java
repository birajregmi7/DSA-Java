public class switch_break_statement {
    public static void main(String[] args) {
        int num = 2;
        switch (num){
            case 1:
                System.out.println("burger");
                break;
            case 2:
                System.out.println("pizza");
                break;
            case 3:
                System.out.println("momo");
                break;
            default:
                System.out.println("Don't be on dream come to reality");
        }
    }
}
